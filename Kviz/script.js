let randomNizPitanja = [];
let randomNiz = () => {
  let promesani = kviz.slice().sort((a,b) => Math.random() - 0.5);
  for(let i = 0; randomNizPitanja.length < 5; i++){
    
    let randomBr = Math.floor(promesani.length * Math.random());
    let randomPitanje = promesani.splice(randomBr,1)[0];
    if(randomNizPitanja.includes(randomPitanje) == false){

      randomNizPitanja.push(randomPitanje);
    }
   }
}
randomNiz();
