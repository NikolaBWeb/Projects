let prikazPitanja = () => {
  randomNizPitanja.forEach((el,i) => {
      let name = `option${i}`;
      let pitanjeCont = document.getElementById(`pitanje${i}`);
      pitanjeCont.innerHTML+= `<p>${i+1}. ${el.pitanje}</p>`;
      el.odgovori.forEach((el,i) => {
        if(i == 0){
          pitanjeCont.innerHTML+=`<input type="radio" name=${name}  id = ${name} checked>`;
          pitanjeCont.innerHTML+=`<label for =${name}> ${el}</label><br>`;
        }else{
          pitanjeCont.innerHTML+=`<input type="radio" name=${name}  id = ${name + i} >`;
          pitanjeCont.innerHTML+=`<label for =${name + i}> ${el}</label><br>`;
        }
       
      })

  })
  

}
prikazPitanja();

let btn = document.getElementById("prikupiOdgovore");


btn.addEventListener("click", (e) => {
    e.preventDefault()
  randomNizPitanja.forEach((el,i) => {
    let element = el;
    let index = i;
    let odgovori = document.querySelectorAll(`input[name = option${i}`);
    let odgovoriP = document.getElementById(`odgovor${i}`)
    let rezultat = "";
    let odgovoriArr = Array.from(odgovori)
    console.log(odgovoriArr);
    for(let i = 0; i < odgovoriArr.length; i++){
      
      if(odgovoriArr[i].checked == true ){
        if(element.odgovori.indexOf(element.odgovori[i]) == element.indexTacnogOdgovora){
          rezultat = `Tacan odgovor na pitanje ${index+1}`;
          odgovoriP.classList.remove("netacan");
          odgovoriP.classList.add("tacan");
          break;
        }
        
       
      }
        rezultat = `Netacan odgovor na pitanje ${index + 1 }`;
        odgovoriP.classList.add("netacan")
      
        
    }
    
    odgovoriP.innerHTML += rezultat;
    odgovoriArr.forEach(el => {
      el.disabled = true;
    })

    
  })
  btn.disabled = true;



})



